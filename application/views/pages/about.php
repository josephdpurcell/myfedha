        <h2>Double entry done right.</h2>

        <p>
            Double entry bookkeeping has been around for over 500 years. It has become the de facto method for tracking finances and has largely remained unchanged. Until now.
        </p>
        <h3>Goals</h3>
        <p>
            People want to know three things: (1) how much do I have, (2) how much will I have, and (3) how can I spend my money better? All it takes to accomplish this is tracking your finances and using some method to plan. MyFedha sets out to accomplish these two goals:
            <ul>
                <li>Track finances using double entry bookkeeping.</li>
                <li>Manage finances intelligently by letting computers do the math.</li>
            </ul>
        </p>
        <h3>Methodology</h3>
        <p>
            Nobody needs to know exactly where their money is going. Trying to do so is nearly impossible and will make you bald faster. Double entry bookkeeping does this really well, and that's why it is such a pain to use.
        </p>
        <p>
            All you need to know are a few accounts to keep track of your finances. Like your checking account. I bet you can list them all on one hand. With these few accounts all transactions you make are covered--no need to enter anything twice.
        </p>
        <p>
            Now comes the tricky part: how do you track where my money is going? Because we only need to know a few main accounts, you first select which account you want to make a transaction on. Then, instead of explicitly stating where your money is going to, you simply tag it. Using these tags we can associate those transactions to general accounts--which is exactly what you want.
        </p>
        <h3>Example</h3>
        <p>
            You click the 'Checking' tab. You add a transaction and tag it as 'gas'. When you look at your budget you don't care whether that $50 went to Shell or Rusty Joe's gas station. All you care about is that I can break down my expenses to 'travel'.
        </p>
        <h3>Into the future...</h3>
        <p>
            Down the road we hope you will be able to completely customize what expense accounts those tags show up in. Imagine being able to drag the 'work' tag into your business account and all transactions tagged both 'gas' and 'work' show up under business finances instead of personal. The sky is the limit.
        </p>
